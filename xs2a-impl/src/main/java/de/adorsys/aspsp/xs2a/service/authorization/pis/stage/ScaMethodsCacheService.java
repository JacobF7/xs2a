package de.adorsys.aspsp.xs2a.service.authorization.pis.stage;

import de.adorsys.psd2.xs2a.core.consent.AspspConsentData;
import de.adorsys.psd2.xs2a.spi.domain.authorisation.SpiAuthenticationObject;
import de.adorsys.psd2.xs2a.spi.domain.psu.SpiPsuData;
import de.adorsys.psd2.xs2a.spi.domain.response.SpiResponse;
import de.adorsys.psd2.xs2a.spi.service.PaymentAuthorisationSpi;
import de.adorsys.psd2.xs2a.spi.service.SpiPayment;
import lombok.Data;
import lombok.extern.slf4j.Slf4j;
import org.springframework.cache.annotation.Cacheable;
import org.springframework.stereotype.Service;

import java.util.List;

@Slf4j
@Data
@Service
public class ScaMethodsCacheService {
    @Cacheable(value = "scaMethodCash", key = "#psuId")
    public SpiResponse<List<SpiAuthenticationObject>> requestAvailableScaMethods(String psuId, PaymentAuthorisationSpi paymentAuthorisationSpi, SpiPsuData psuData, SpiPayment payment, AspspConsentData aspspConsentData) {
        return paymentAuthorisationSpi.requestAvailableScaMethods(psuData, payment, aspspConsentData);
    }
}

